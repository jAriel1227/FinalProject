    private void Llenar() {
        RegistroProductos pr = new RegistroProductos();
        int filaseleccionada = jTable1.getSelectedRow();
        if (filaseleccionada >= 0) {
            this.dispose();
            pr.ID = Integer.valueOf(modelo.getValueAt(filaseleccionada, 0).toString());
            pr.txtNombre.setText(modelo.getValueAt(filaseleccionada, 1).toString());
            pr.txtMarca.setText(modelo.getValueAt(filaseleccionada, 2).toString());
            pr.txtCategoria.setText(modelo.getValueAt(filaseleccionada, 3).toString());
            pr.txtPrecio.setText(modelo.getValueAt(filaseleccionada, 4).toString());
            pr.txtCantidad.setText(modelo.getValueAt(filaseleccionada, 5).toString());
            pr.editar = true;
            pr.setVisible(true);
        }
        
    }

