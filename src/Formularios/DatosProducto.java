    public void LlenarTabla() {
        Productos productos = new Productos();
        modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        
        ClassConectar conexion = new ClassConectar();
        try {
            ResultSet set = null;
            PreparedStatement state = conexion.Conexion().prepareStatement("SELECT * FROM productos");
            set = state.executeQuery();
            
            while (set.next()) {
                productos.IdProductos = set.getInt(1);
                productos.NombreProductos = set.getString(2);
                productos.MarcaProductos = set.getString(3);
                productos.CategoriaProductos = set.getString(4);
                productos.PrecioProducto = set.getInt(5);
                productos.CantidadProducto = set.getInt(6);
                modelo.addRow(new Object[]{productos.IdProductos, productos.NombreProductos, productos.MarcaProductos, productos.CategoriaProductos, productos.PrecioProducto, productos.CantidadProducto});
            }
        } catch (Exception e) {
        }
    }

